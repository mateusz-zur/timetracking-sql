--  DDL for View CZAS_2014_2016
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "ZUR"."CZAS_2014_2016" ("2014-2016") AS 
  SELECT SUM(ZADANIE.CZAS) AS "2014-2016" FROM ZADANIE
WHERE ZADANIE.DATA BETWEEN '14-01-01' AND '14-12-31'
UNION
SELECT SUM(ZADANIE.CZAS) FROM ZADANIE
WHERE ZADANIE.DATA BETWEEN '15-01-01' AND '15-12-31'
UNION
SELECT SUM(ZADANIE.CZAS) FROM ZADANIE
WHERE ZADANIE.DATA BETWEEN '16-01-01' AND '16-12-31'
;
